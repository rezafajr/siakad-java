    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        // TODO add your handling code here:
        if(txt_nidn.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null,"NIDN Tidak Boleh Kosong");
        }
        else if(txt_nama.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null,"Nama Dosen Tidak Boleh Kosong");
        }
        else{
            String nidn = String.valueOf(txt_nidn.getText());
            String nama_dosen = String.valueOf(txt_nama.getText());
            String v_jk = null;
        
                if(rd_lk.isSelected()){
                    v_jk = "L";
                }else if (rd_pr.isSelected()){
                    v_jk = "P";
                }
            String v_alamat = String.valueOf(txt_alamat.getText());
            try{
                java.sql.Statement statement = (java.sql.Statement)KoneksiDB.getConnection().createStatement();
                
                statement.executeUpdate("update tb_dosen set nama_dosen = '"+ nama_dosen +"', jk_dosen = '"+ v_jk +"',  alamat_dosen = '"+ v_alamat +"'  where nidn_dosen = '"+ nidn +"'");
                statement.close();
                JOptionPane.showMessageDialog(null,"Data berhasil di input");
                
                txt_nidn.setText("");
                txt_nama.setText("");
                txt_alamat.setText("");
                txt_nidn.requestFocus();
                bg_jk.clearSelection();
                txt_nidn.requestFocus();
            }catch(Exception e){
                
            }
        }
        
    }//GEN-LAST:event_btn_editActionPerformed

