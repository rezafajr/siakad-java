    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        if(txt_nidn.getText().toString().equals("")){
            JOptionPane.showMessageDialog(null, "NIDN tidak boleh kosong!");
        }else if(txt_nama.getText().toString().equals("")){
            JOptionPane.showMessageDialog(null, "Nama tidak boleh kosong!");
        }else if(txt_alamat.getText().toString().equals("")){
            JOptionPane.showMessageDialog(null, "Alamat tidak boleh kosong!");
        }else{
            if(rd_lk.isSelected() || rd_pr.isSelected()){
                String nidn, nama, jk = null, alamat;
                nidn = String.valueOf(txt_nidn.getText());
                nama = String.valueOf(txt_nama.getText());
                alamat = String.valueOf(txt_alamat.getText());
                
                if(rd_lk.isSelected()){
                    jk = "L";
                }else if(rd_pr.isSelected()){
                    jk = "P";
                }
            
                try{
                    Statement statement = (Statement)KoneksiDB.getConnection().createStatement();
                    statement.executeUpdate("INSERT INTO tb_dosen Values('"+nidn+"','"+nama+"','"+jk+"','"+alamat+"')");
                    statement.close();
                    JOptionPane.showMessageDialog(null, "Data Dosen Berhasil Di Input!!!");

    //                txt_nidn.setText("");
    //                txt_nama.setText("");
                    txt_nidn.requestFocus();

                }catch(Exception e){
                    JOptionPane.showMessageDialog(null, "Koneksi Gagal");
                }
                    
            }else{
                JOptionPane.showMessageDialog(null, "Pilih Jenis Kelamin CUY!!!");
            }
            
            
            
        }
    }//GEN-LAST:event_btn_simpanActionPerformed

